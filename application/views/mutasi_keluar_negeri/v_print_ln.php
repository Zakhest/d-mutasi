    function convertToIndonesianDate(dateString) {
    const monthsIndo = [
        'Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni',
        'Juli', 'Agustus', 'September', 'Oktober', 'November', 'Desember'
    ];

    // Bersihkan teks dari karakter selain angka dan "-"
    dateString = dateString.replace(/[^0-9-]/g, '').trim(); // Hilangkan huruf M

    const date = new Date(dateString);
    if (isNaN(date)) return dateString; // Jika format salah, tampilkan teks asli

    const day = String(date.getDate()).padStart(2, '0');
    const monthName = monthsIndo[date.getMonth()];
    const year = date.getFullYear();

    return `${day} ${monthName} ${year}`;
}

// Konversi semua elemen dengan class 'convert-full-date'
window.onload = function() {
    const dateElements = document.querySelectorAll('.convert-full-date');
    dateElements.forEach(element => {
        const originalDate = element.innerText.trim();
        element.innerText = convertToIndonesianDate(originalDate);
    });
};

